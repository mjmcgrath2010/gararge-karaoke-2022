import Head from "next/head";
import useSWR from "swr";

import DataTable from "react-data-table-component";
import Container from "../components/Container";
import Heading from "../components/Heading";

const columns = [
  {
    name: "Title",
    selector: (row: any) => row.title,
    sortable: true,
  },
  {
    name: "Artist",
    selector: (row: any) => row.artist,
    sortable: true,
  },
  {
    name: "Disk Number",
    selector: (row: any) => row.diskNumber,
    sortable: true,
  },
];

export default function Home() {
  const { data, error } = useSWR("/api/songs", () => {
    return fetch("/api/songs").then((res) => res.json());
  });

  if (!data) return <div>Loading...</div>;

  return (
    <>
      <Head>
        <title>Garage Karaoke</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Container direction="column">
        <Heading>All Songs</Heading>
        <Container padding={0} direction="column">
          <DataTable pagination columns={columns} data={data} />
        </Container>
      </Container>
    </>
  );
}
